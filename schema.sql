ALTER TABLE item ADD CONSTRAINT fk_source FOREIGN KEY (source_id) REFERENCES source(id) ON DELETE CASCADE;
ALTER TABLE item ADD CONSTRAINT fk_label FOREIGN KEY (label_id) REFERENCES label(id) ON DELETE CASCADE;
